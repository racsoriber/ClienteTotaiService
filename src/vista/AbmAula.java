    private void init() {
        this.tablaAulas.setModel(modeloAulas);
        cargarDatos();
    }

    private void cargarDatos() {
        List<String> list = servicioAula.getAulas();
        List<Object[]> data = new ArrayList<Object[]>();
        for (String csv : list) {
            String[] array = csv.split(",");
            data.add(new Object[]{array[0], array[1]});
        }
        this.modeloAulas.setDatos(data);
    }

    private void crearNuevo() {
        this.txtId.setEditable(true);
        this.txtId.setText("");
        this.spinCapacidad.setValue(25);
        enableModificar(false);

    }

    private void addAula() {
        Integer capacidad = Integer.parseInt(spinCapacidad.getValue().toString());
        if (capacidad < 5 || txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Datos invalidos");
        } else {
            String resultado = servicioAula.addAula(txtId.getText(), capacidad);
            cargarDatos();
            JOptionPane.showMessageDialog(this, resultado);
        }
    }

    private void selectAula() {
        int index = tablaAulas.getSelectedRow();
        if (index > -1) {
            this.txtId.setEditable(false);
            this.txtId.setText(modeloAulas.getValueAt(index, 0).toString());
            this.spinCapacidad.setValue(modeloAulas.getValueAt(index, 1));
            enableModificar(true);
        }
    }

    private void enableModificar(boolean enable) {
        this.btnAgregar.setEnabled(!enable);
        this.btnModificar.setEnabled(enable);
        this.btnEliminar.setEnabled(enable);
    }

    private void modificarAula() {
        Integer capacidad = Integer.parseInt(spinCapacidad.getValue().toString());
        if (capacidad < 5) {
            JOptionPane.showMessageDialog(this, "Datos invalidos");
        } else {
            String resultado = servicioAula.modificarAula(txtId.getText(), capacidad);
            cargarDatos();
            JOptionPane.showMessageDialog(this, resultado);
        }
    }

